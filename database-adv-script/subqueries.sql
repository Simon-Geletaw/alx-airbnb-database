SELECT
	PROPERTY_ID,
	AVG(RATING)
FROM
	AIRBNB_SCHEMA."Review"
GROUP BY
	PROPERTY_ID
HAVING
	AVG(RATING) > 4.0;

SELECT 
    PROPERTY_ID,
    AVG(RATING) AS average_rating
FROM 
    AIRBNB_SCHEMA."Review"
GROUP BY 
    PROPERTY_ID
HAVING 
    AVG(RATING) > (
        SELECT 4.0
    );
SELECT
	U.FIRST_NAME,
	U.LAST_NAME
FROM
	AIRBNB_SCHEMA."User" U
WHERE
	(
		SELECT
			COUNT(*)
		FROM
			AIRBNB_SCHEMA."Booking" B
		WHERE
			B.USER_ID = U.USER_ID
	) > 3;